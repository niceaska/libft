#include "libft.h"

char	*ft_strlowcase(char *str)
{
	size_t i;

	i = 0;
	while (str[i])
	{
		str[i] += (str[i] >= 'A' && str[i] <= 'Z') ? 32 : 0;
		i++;
	}
	return (str);
}
