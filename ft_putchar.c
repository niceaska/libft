#include "libft.h"

void	ft_putchar(char c)
{
	unsigned char ch;
	unsigned char u[2];

	ch = (unsigned char)c;
	if (ch < 128)
		write(1, &c, 1);
	else
	{
		u[0] = 192 | (ch >> 6);
		u[1] = 128 | (63 & ch);
		write(1, u, 2);
	}
}
